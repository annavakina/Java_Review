package week01_review;

public class EscapeSequences {

    public static void main(String[] args) {

      // \t

        System.out.println("\tHello Batch 28 \tStudents");

        // \n
        System.out.println("Hello Students\n\t How are you today?");

        // \"
        System.out.println("Hello \" Cydeo\" Batch 28 students");

       // \\
        System.out.println("    ^");
        System.out.println("   / \\");
        System.out.println("  /   \\");
        System.out.println(" /     \\");
        System.out.println(" -------");


    }








}

